  function getAssetUrl(relativePath) {
    if (
      typeof chrome !== 'undefined' &&
      chrome.runtime &&
      typeof chrome.runtime.getURL === 'function'
    ) {
      return chrome.runtime.getURL(relativePath);
    }

    if (
      typeof browser !== 'undefined' &&
      browser.runtime &&
      typeof browser.runtime.getURL === 'function'
    ) {
      return browser.runtime.getURL(relativePath);
    }

    return null;
  }

  async function loadFilterData() {
    if (filterDataLoaded) {
      return filterData;
    }

    if (filterDataLoadingPromise) {
      return filterDataLoadingPromise;
    }
    filterDataLoadingPromise = (async() => {
      const data = cloneDefaultData();

      const fetchFn =
        typeof globalThis !== 'undefined' && typeof globalThis.fetch === 'function'
          ? globalThis.fetch.bind(globalThis)
          : null;

      if (fetchFn) {
        const entries = Object.entries(DATA_FILE_PATHS);
        await Promise.allSettled(
          entries.map(async([key, path]) => {
            const assetUrl = getAssetUrl(path);
            if (!assetUrl) {
              return;
            }

            try {
              const response = await fetchFn(assetUrl);
              if (!response.ok) {
                Logger.warn(`Failed to load data file ${path}: ${response.status}`);
                return;
              }

              const payload = await response.json();
              if (!Array.isArray(payload)) {
                Logger.warn(`Unexpected data format for ${path}`);
                return;
              }

              const isCaseInsensitiveKey =
                key === 'disableTerms' ||
                key === 'aiOverviewPatterns' ||
                key === 'aiIndicatorPhrases' ||
                key === 'lowQualityDomains' ||
                key === 'adIndicatorLabels';

              if (isCaseInsensitiveKey) {
                data[key] = sanitizeCaseInsensitiveArray(payload);
              } else if (key === 'aiSelectors' || key === 'adSelectors') {
                data[key] = sanitizeSelectors(payload);
              }
            } catch (error) {
              Logger.warn(`Error loading data file ${path}`, error);
            }
          })
        );
      }

      filterData = data;
      filterDataLoaded = true;
      return filterData;
    })();

    try {
      return await filterDataLoadingPromise;
    } finally {
      filterDataLoadingPromise = null;
    }
  }

  function containsAiOverviewHeading(text) {
    if (!text) {
      return false;
    }
    const normalized = text.toLowerCase();
    return filterData.aiOverviewPatterns.some(pattern => normalized.includes(pattern));
  }

  function containsAiIndicator(text) {
    if (!text) {
      return false;
    }
    const normalized = text.toLowerCase();
    return filterData.aiIndicatorPhrases.some(phrase => normalized.includes(phrase));
  }

  function isDomainWhitelisted(hostname) {
    if (!hostname) {
      return false;
    }

    if (!Array.isArray(filterSettings.customWhitelist)) {
      return false;
    }

    const normalized = hostname.toLowerCase();
    return filterSettings.customWhitelist.some(entry => {
      if (typeof entry !== 'string') {
        return false;
      }
      const candidate = entry.trim().toLowerCase();
      if (!candidate) {
        return false;
      }
      return normalized === candidate || normalized.endsWith(`.${candidate}`);
    });
  }

  function isLowQualityHostname(hostname) {
    if (!hostname) {
      return false;
    }

    const normalized = hostname.toLowerCase();
    if (isDomainWhitelisted(normalized)) {
      return false;
    }

    return filterData.lowQualityDomains.some(domain =>
      normalized === domain || normalized.endsWith(`.${domain}`)
    );
  }

  function extractHostname(urlString, depth = 0) {
    if (!urlString || depth > 3) {
      return null;
    }

    try {
      const parsed = new URL(urlString, window.location.origin);
      const hostname = parsed.hostname.toLowerCase();

      if (/(^|\.)google\./i.test(hostname)) {
        const candidateParams = ['url', 'q', 'u', 'target', 'continue'];
        for (const param of candidateParams) {
          const paramValue = parsed.searchParams.get(param);
          if (paramValue) {
            const extracted = extractHostname(paramValue, depth + 1);
            if (extracted) {
              return extracted;
            }
          }
        }
      }

      return hostname;
    } catch {
      return null;
    }
  }

