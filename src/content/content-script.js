  function isLowQualitySite(result) {
    try {
      const anchorElement = result.querySelector('a');
      if (!anchorElement || !anchorElement.href) {
        return false;
      }
      
      const url = anchorElement.href;
      const hostname = new URL(url).hostname;
      return isLowQualityHostname(hostname);
    } catch {
      return false;
    }
  }

  function removeElement(element, type = 'unknown') {
    if (
      !extensionEnabled ||
      !element ||
      element.hasAttribute('data-removed') ||
      element.hasAttribute('data-clean-search-removed') ||
      safetyCounter >= MAX_REMOVALS_PER_SCAN
    ) {
      return;
    }
    
    // Increment safety counter to prevent excessive removals
    safetyCounter++;

    try {
      if (!element.hasAttribute('data-clean-search-original-display')) {
        let originalDisplay = '';
        try {
          originalDisplay = window.getComputedStyle(element).display || '';
        } catch {
          originalDisplay = '';
        }
        element.setAttribute('data-clean-search-original-display', originalDisplay);
      }

      element.setAttribute('data-clean-search-type', type);
      // Mark element as removed by either system so other code won't try again
      element.setAttribute('data-removed', 'true');
      element.setAttribute('data-clean-search-removed', 'true');
      element.style.display = 'none';

      const statType =
        type === 'ai'
          ? 'aiElementsRemoved'
          : type === 'low-quality'
            ? 'lowQualitySitesRemoved'
            : type === 'ad'
              ? 'adsRemoved'
              : 'totalElementsRemoved';

      updateStats(statType, 1);

      if (filterSettings.showReplacementPlaceholders) {
        createRemovalPlaceholder(element, type);
      }

      // Log the removed DOM node (object) so developers can inspect it when logging is enabled
      Logger.info('Removed element', { type, element });
    } catch (error) {
      Logger.warn(`Error removing ${type} element:`, error);
    }
  }

